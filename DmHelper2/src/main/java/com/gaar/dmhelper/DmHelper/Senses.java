package com.gaar.dmhelper.DmHelper;

public class Senses {

	//Sense prompt variables
	private String sight = "What do they see?";
	private String sound = "What do they hear?";
	private String smell = "What do they smell?";
	private String taste = "What do they taste?";
	private String touch = "what do they feel?";
	
	//GETTERS + SETTERS
	public String getSight() {
		return sight;
	}
	public void setSight(String sight) {
		this.sight = sight;
	}
	public String getSound() {
		return sound;
	}
	public void setSound(String sound) {
		this.sound = sound;
	}
	public String getSmell() {
		return smell;
	}
	public void setSmell(String smell) {
		this.smell = smell;
	}
	public String getTaste() {
		return taste;
	}
	public void setTaste(String taste) {
		this.taste = taste;
	}
	public String getTouch() {
		return touch;
	}
	public void setTouch(String touch) {
		this.touch = touch;
	}
	
	
}
